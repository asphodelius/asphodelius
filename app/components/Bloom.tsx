export function Bloom({ className, locale, word }: BloomProps) {
  const [isActive, setIsActive] = useState(false);
  const isEnglishCustom = locale === "en";

  useEffect(() => {
    let frameA = 0;
    let frameB = 0;

    frameA = window.requestAnimationFrame(() => {
      frameB = window.requestAnimationFrame(() => {
        setIsActive(true);
      });
    });

    return () => {
      window.cancelAnimationFrame(frameA);
      window.cancelAnimationFrame(frameB);
    };
  }, []);

  return (
    <span
      className={cn(
        "bloom-wordmark relative isolate inline-block align-baseline whitespace-nowrap text-inherit",
        className,
      )}
      data-bloom-active={isActive ? "true" : "false"}
      data-locale={locale}
      style={
        {
          "--bloom-sequence-offset": isEnglishCustom ? "0.48s" : "0.72s",
        } as CSSProperties
      }
    >
      <span className="relative z-[2] block">
        {word ?? (locale === "ru" ? "\u0440\u0430\u0441\u0446\u0432\u0435\u0442\u0430\u0435\u0442" : "bloom")}
      </span>

      {isEnglishCustom ? renderEnglishWordmark() : renderRussianWordmark()}
    </span>
  );
}

function renderShape(shape: ShapeSpec, key: string) {
  if (shape.kind === "circle") {
    return (
      <circle
        key={key}
        className="bloom-outline bloom-drawable"
        cx={shape.cx}
        cy={shape.cy}
        r={shape.r}
        pathLength={1}
      />
    );
  }

  return (
    <path
      key={key}
      className="bloom-outline bloom-drawable"
      d={shape.d}
      pathLength={1}
    />
  );
}

function renderAssetWordmark<AssetId extends string>(
  assets: Record<AssetId, AssetSpec>,
  layout: readonly WordmarkPlacement<AssetId>[],
  viewBox: string,
  className: string,
) {
  return (
    <svg
      className={className}
      viewBox={viewBox}
      preserveAspectRatio="xMidYMid meet"
      fill="none"
      aria-hidden="true"
    >
      {layout.map((item) => {
        const asset = assets[item.asset];
        const [, , assetWidth, assetHeight] = asset.viewBox.split(" ").map(Number);

        return (
          <g key={item.id} transform={`translate(${item.x} ${item.y})`}>
            <g
              className="bloom-asset"
              style={
                {
                  "--bloom-breath": item.breathDuration,
                  "--bloom-delay": `${item.delay}s`,
                  "--bloom-draw-duration": item.drawDuration,
                  "--bloom-drift-y": item.driftY,
                  "--bloom-stroke": item.stroke,
                } as CSSProperties
              }
            >
              <svg
                viewBox={asset.viewBox}
                width={assetWidth}
                height={assetHeight}
                overflow="visible"
              >
                {asset.shapes.map((shape, index) => renderShape(shape, `${item.id}-${index}`))}
              </svg>
            </g>
          </g>
        );
      })}
    </svg>
  );
}

